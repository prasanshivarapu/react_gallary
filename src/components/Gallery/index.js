class Gallery extends Component {
  state = {resultItem: imagesList[0].id}

  button = () => {
    const {resultItem} = this.state
    const resultone = imagesList.filter(each => each.id === resultItem)
    return resultone
  }

  realimage = id => {
    this.setState({resultItem: id})
  }

  render() {
    const {resultItem} = this.state
    const buttonone = this.button()
    return (
      <div className="greet5">
        {buttonone.map(each => (
          <img className="image2" src={each.imageUrl} alt="avatar" />
        ))}
        <h1>Nature photography</h1>

        <p className="para">by rahul</p>

        <div className="greet1">
          {imagesList.map(each => (
            <ThumbnailItem
              wanted={each}
              key={each.id}
              realimage={this.realimage}
              pick={resultItem === each.id}
            />
          ))}
        </div>
      </div>
    )
  }
}

